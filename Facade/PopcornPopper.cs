using System;

public class PopcornPopper
{
    public void On()
    {
        System.Console.WriteLine("Popcorn popper on");
    }

    public void Pop()
    {
        System.Console.WriteLine("Pop, ..., pop, pop, pop");
    }

    public void Off()
    {
        System.Console.WriteLine("Popcorn popper off");
    }
}